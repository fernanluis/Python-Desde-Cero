# Ingreso de datos por consola.
name = input("Name: ")
print("Hello, " + name)

# Operaciones con caracteres (sin operador +), usando cadenas formateadas (formmated strings) o f-strings

name = input("Name: ")
print(f"Hello, {name}")

# Conectando una función, escribiendo en una sola línea

print(f"Hello, {input('name: ')}")

